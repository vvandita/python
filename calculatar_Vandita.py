print("start of program")


x = input("enter first number: ")
x = int(x)
y = input("enter second number: ")
y = int(y)

w = input("do you want to + - * /")

if(w == "+"):
    answer = x+y
    print(x,"+",y,"=", answer )
    
elif(w == "-"):
    answer = x-y
    print(x,"-",y,"=", answer)

elif(w == "*"):
    answer = x*y
    print(x,"*",y,"=", answer )

elif(w == "/"):
    answer = x/y
    print(x,"/",y,"=", answer )
        




 















































































    
